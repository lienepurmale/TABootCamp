package com.bootcampexercise.module2.activity;

public class AddWholeNum {
    public static void main (String []args){
        int sum=50;
        for (int i = 50; i < 100; i++) {
            sum=sum+i;
        }
            System.out.println("Sum of whole numbers from 50 to is 100 is " + sum);
    }
}
